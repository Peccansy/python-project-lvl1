"""Реализации вычислителей выражений."""


def subtract(left, right):
    """Разность.

    Args:
        left: левый операнд
        right: правый операнж

    Returns:
        разность между left и right
    """
    return left - right


def summ(left, right):
    """Сумма.

    Args:
        left: левый операнд
        right: правый операнж

    Returns:
        сумму left и right
    """
    return left + right


def mul(left, right):
    """Произведение.

    Args:
        left: левый операнд
        right: правый операнж

    Returns:
        произведение left и right
    """
    return left * right
